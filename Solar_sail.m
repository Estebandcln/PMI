plot(d/150,T,'k','LineWidth',1.5);

title(['Thrust generated by a  ',num2str(S),' m^2 solar sail (N)']);
xlabel('Distance to the Sun (ua)');
ylabel('Thrust (N)');
box on;axis tight;grid on;
set(gca,'fontsize', lgndsize,'FontWeight','normal','FontName',NameFont,'YScale', 'log');


