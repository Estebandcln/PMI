clc;clear all;close all;
lgndsize=14;labsize=15;txtsize=16;arrowwidth=1;linsize=1.5;NameFont='Cambria';

pi=3.1415926535;
theta=45*pi/180;              %%rad
c=299792458;                  %%m/s
d=linspace(1,6000,1000);        %%10^9 m : Pluto is 5 900 898 440 km away
S=10000;                      %%m2

